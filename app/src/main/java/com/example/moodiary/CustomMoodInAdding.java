    public CustomMoodInAdding(Context context, Integer[] items) {
        this.context = context;
        this.items = items;
    }

    @Override
    public int getCount() {
        return items.length;
    }

    @Override
    public Object getItem(int i) {
        return null;
    }

    @Override
    public long getItemId(int i) {
        return 0;
    }

    @Override
    public View getView(int position, View convertView, ViewGroup parent) {
        LayoutInflater inflater = ((Activity) context).getLayoutInflater();
        View row = inflater.inflate(R.layout.custom_adding_mood, null);
        ImageView icon = row.findViewById(R.id.addingMood);
        icon.setImageResource(items[position]);
        return(row);
    }
